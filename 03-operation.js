/// Operation Math
/**
 เรียงตามลำดับความสำคัญ และ จากซ้ายไปขวา
() bracket หรือ วงเล็บ
*  multiply หรือ การคูณ
/  divide หรือ การหาร
%  moderation หรือ การหาเศษ
+  plus หรือ การบวก
-  minus หรือ การลบ
*/
console.log(5 * 5 + 20);
console.log(5 * 5 / (3 + 20));
console.log("5 % 3 = ", 5 % 3);
console.log("100 % 7 = ", 100 % 7);

/// Math
console.log(5 * 5 * 5 * 5 * 5 * 5 * 5);
console.log(Math.pow(5 , 7));
console.log(Math.sqrt(3));
const ran = Math.random() * 100;
console.log("random: ", ran);
console.log("random Florr: ", Math.floor(ran));
console.log("random Ceil ", Math.ceil(ran));
console.log("random Round: ", Math.round(ran));
